package variable;

public class Type1 {
    public static void main(String[] args) {
        int a = 100; // 정수, 1, 100, 100,
        double b = 20.5; // 실수 1.1 2.3
        boolean c = true; // 불리언, 참-거짓 논리값 true-false
        char d = 'A';   // 캐릭터, 문자 하나, 홑따옴표
        String e = "안녕하세요";   // 문자열, 쌍따옴표
    }

}
